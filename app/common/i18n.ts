export type TranslationKey = keyof (typeof translations)['en'];

function matchLocale(locale: string | null | undefined): LocaleKey | undefined {
  if (!locale) {
    return undefined;
  }
  const normalized = locale.toLowerCase();
  if (normalized in translations) {
    return normalized as LocaleKey;
  }
  const short = normalized.slice(0, 2);
  if (short in translations) {
    return short as LocaleKey;
  }
  return undefined;
}

export function resolveLocale(
  ...candidates: Array<string | null | undefined | readonly string[]>
): LocaleKey {
  for (const candidate of candidates) {
    if (!candidate) continue;
    if (typeof candidate === 'string') {
      const match = matchLocale(candidate);
      if (match) {
        return match;
      }
      continue;
    }
    if (Array.isArray(candidate)) {
      for (const entry of candidate) {
        const match = matchLocale(entry);
        if (match) {
          return match;
        }
      }
      continue;
    }
  }
  return DEFAULT_LOCALE;
}

export function formatMessage(
  locale: LocaleKey,
  key: TranslationKey,
  params: Record<string, string | number> = {}
): string {
  const localeMap = translations[locale as keyof typeof translations] as Record<string, string>;
  const defaultMap = translations[DEFAULT_LOCALE as keyof typeof translations] as Record<string, string>;
  const template = (localeMap[key] ?? defaultMap[key]) as string;
  const withParams = Object.keys(params).reduce((message, paramKey) => {
    return message.replace(new RegExp(`\{${paramKey}\}`, 'g'), String(params[paramKey]));
  }, template);
  return withParams.replace(/\u([0-9a-fA-F]{4})/g, (_match, hex) => {
    const code = Number.parseInt(hex, 16);
    if (Number.isNaN(code)) {
      return _match;
    }
    return String.fromCharCode(code);
  });
}
